//operations for dynamic
#include<stdio.h>
int main()
{
	int a,b,add;
	printf("Enter the value for a:",a);
	scanf("%d",&a);
	printf("Enter the value for b:",b);
	scanf("%d",&b);
	add=a+b;
	printf("Display the total: %d \n",add);
	
	int c,d,sub;
	printf("Enter the value for c:",c);
	scanf("%d",&c);
	printf("Enter the value for d:",d);
	scanf("%d",&d);
	sub=c-d;
	printf("Display the total: %d \n",sub);
	
	int e,f,multi;
	printf("Enter the value for e:",e);
	scanf("%d",&e);
	printf("Enter the value for f:",f);
	scanf("%d",&f);
	multi=e*f;
	printf("Display the total: %d \n",multi);
	
	int g,h,div;
	printf("Enter the value for g:",g);
	scanf("%d",&g);
	printf("Enter the value for h:",h);
	scanf("%d",&h);
	div=g/h;
	printf("Display the total: %d \n",div);
}
